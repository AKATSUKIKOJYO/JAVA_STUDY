public class GradeElseTest {

	public static void main(String[] args) {
		int 홍길동 = 92;
		int 홍길순 = 85;
		
		if ( 홍길동 >= 90) {
			System.out.println("홍길동: A학점입니다.");
		} else {
			System.out.println("홍길동: A학점을 받지 못했습니다.");
		}
		if ( 홍길순 >= 90) {
			System.out.println("홍길순: A학점입니다.");
		} else {
			System.out.println("홍길순: A학점을 받지 못했습니다.");
		}

	}

}
